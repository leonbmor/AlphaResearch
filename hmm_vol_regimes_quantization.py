# Model version: bump when the emission model / fit logic changes (e.g. Gaussian
# -> Student-t), so cached results from a different model are never silently mixed
# with new ones. Incremental runs require a matching model_version; a mismatch is
# treated as "no cache for this asset" (i.e. recompute), never a silent reuse.
MODEL_VERSION = 'gauss3_v1'

RESULTS_TBL   = 'hmm_regime_cache'
STATE_TBL     = 'hmm_warmstart_state'

